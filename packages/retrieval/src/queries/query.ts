import type { JsonValue } from '@rag-sdk/core'

/**
 * 业务调用方提交的原始检索请求。
 *
 * 该结构只描述进入检索流程前的输入，不包含查询向量等由检索前处理器生成的执行数据。
 */
export interface RetrievalRequest {
    /**
     * 业务调用方提供的原始查询文本。
     *
     * 该文本尚未保证经过校验、标准化或改写，检索前处理器可以在生成可执行查询时调整它。
     */
    text: string

    /**
     * 用于限制召回范围的过滤条件。
     *
     * `retrieval` 只负责传递该对象，不定义字段名称、操作符或匹配规则，也不在此承载业务权限逻辑。
     * 具体语义由对应的检索器或 adapter 解释。
     */
    filter?: Record<string, JsonValue>

    /**
     * 传递给特定检索器或 adapter 的额外参数。
     *
     * 该对象用于承载标准查询字段无法表达的外部能力配置；`retrieval` 不解释其中的字段，
     * 调用方应只提供目标实现明确支持的参数。
     */
    params?: Record<string, JsonValue>

    /**
     * 期望检索器在召回阶段返回的最大候选数量。
     *
     * 该值不代表最终结果数量；检索器可能返回更少的候选，检索后处理器也可能继续过滤、
     * 去重或截断结果。未提供时，由具体检索器决定默认召回数量。
     */
    topK?: number
}

/**
 * 检索前阶段整理后，可直接交给检索器执行的查询。
 *
 * 该结构继承原始请求字段；检索前处理器可以更新查询文本、过滤条件、参数和召回数量，
 * 也可以补充具体检索器执行时所需的查询向量。
 */
export interface RetrievalQuery extends RetrievalRequest {
    /**
     * 当前查询文本对应的向量表示。
     *
     * 该字段通常由查询向量生成处理器提供，主要供向量检索器使用。文本检索器等不需要
     * 查询向量的实现可以忽略或不提供该字段。
     */
    embedding?: number[]
}
